#include <asm.h>

t_op				*get_op_by_id(size_t id)
{
	size_t				i;

	i = 0;
	while (i < 16)
	{
		if (op_tab[i].op_code == id)
			return (&(op_tab[i]));
		i++;
	}
	return (&op_tab[16]);
}
